std::wstring Parameter::get(std::wstring name)
{
	auto it = _Map.find(name);
	if (it == _Map.end())
	{
		return L"";
	}
	return it->second;
}

void Parameter::set(std::wstring name, std::wstring value)
{
	_Map[name] = value;
}




