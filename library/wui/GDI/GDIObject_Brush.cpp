/////////////////////////////////////////////////////////////////////////////
//===========================================================================
Brush::Brush(
	COLORREF color
)
{
	_BrushHandle = createBrush(
		color
	);
	if (_BrushHandle == nullptr)
	{
		throw std::runtime_error("Brush::Brush(): createBrush() failed");
	}
}

//===========================================================================
Brush::~Brush()
{
	::DeleteObject(_BrushHandle);
}

//===========================================================================
HBRUSH Brush::createBrush(
	COLORREF color
)
{
	return ::CreateSolidBrush(color);
}

//===========================================================================
HBRUSH Brush::select(HDC hdc)
{
	return reinterpret_cast<HBRUSH>(::SelectObject(hdc, *this));
}





