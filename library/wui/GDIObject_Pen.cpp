/////////////////////////////////////////////////////////////////////////////
//===========================================================================
Pen::Pen(
	int style, int width, COLORREF color
)
{
	_PenHandle = createPen(
		style, width, color
	);
	if (_PenHandle == nullptr)
	{
		throw std::runtime_error("Pen::Pen(): createPen() failed");
	}
}

//===========================================================================
Pen::~Pen()
{
	::DeleteObject(_PenHandle);
}

//===========================================================================
HPEN Pen::createPen(
	int style, int width, COLORREF color
)
{
	return ::CreatePen(style, width, color);
}

//===========================================================================
HPEN Pen::select(HDC hdc)
{
	return reinterpret_cast<HPEN>(::SelectObject(hdc, *this));
}





